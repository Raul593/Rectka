let traductor = 'gato';
switch (traductor) {
    case "gato":
        console.log("Cat")
        break;
    case "perro":
        console.log("Dog")
        break;
    case "puerta":
        console.log("Door")
        break;
    case "ventana":
        console.log("Window")
    default:
        console.log("La palabra ingresada es incorrecta")
        break;
}